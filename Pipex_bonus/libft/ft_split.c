#include "libft.h"

static int	ft_wordlen(char const *s, char c)
{
	int	len;

	len = 0;
	while (*s && *s != c)
	{
		len++;
		s++;
	}
	return (len);
}

static int	ft_countwords(char const *s, char c)
{
	int	count;

	count = 0;
	while (*s)
	{
		if (*s != c)
		{
			count++;
			s += ft_wordlen(s, c);
		}
		else
			s++;
	}
	return (count);
}

static void	*free_strs(char **strs)
{
	int	i;

	i = 0;
	while (strs[i])
		free(strs[i++]);
	free(strs);
	return (NULL);
}

char	**ft_split(char const *str, char c)
{
	char	**strs;
	int		i;
	int		count;

	if (!str)
		return (NULL);
	count = ft_countwords(str, c);
	strs = malloc(sizeof(char *) * (count + 1));
	if (!strs)
		return (NULL);
	strs[count] = NULL;
	i = 0;
	while (*str)
	{
		if (*str != c)
		{
			strs[i] = ft_substr(str, 0, ft_wordlen(str, c));
			if (!strs[i++])
				return (free_strs(strs));
			str += ft_wordlen(str, c);
		}
		else
			str++;
	}
	return (strs);
}
